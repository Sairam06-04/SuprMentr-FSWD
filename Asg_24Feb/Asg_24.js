// Write JS programs for calculator operations inside the browser console
// 1. Addition
let num1 = 10;
let num2 = 5;
let sum = num1 + num2;
console.log("Addition: " + sum);

// 2. Subtraction
let difference = num1 - num2;
console.log("Subtraction: " + difference);

// 3. Multiplication
let product = num1 * num2;
console.log("Multiplication: " + product);

// 4. Division  
let quotient = num1 / num2;
console.log("Division: " + quotient);

// 5. Modulus
let modulus = num1 % num2;
console.log("Modulus: " + modulus);

// 6. Exponentiation
let exponentiation = num1 ** num2;
console.log("Exponentiation: " + exponentiation);

// 7. Increment
num1++;
console.log("Increment: " + num1);

// 8. Decrement
num2--;
console.log("Decrement: " + num2);

// 9. Square Root
let squareRoot = Math.sqrt(num1);
console.log("Square Root: " + squareRoot);

// 10. Absolute Value
let absoluteValue = Math.abs(-num1);
console.log("Absolute Value: " + absoluteValue);

